// This method generates a new employee ID and returns it as a string.

    private void generateEmployeeID() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `employee` ORDER BY `employee_id` DESC LIMIT 1");
            if (resultSet.next()) {
                lastEmployeeID = resultSet.getString("employee_id");
            }else{
                lastEmployeeID = "EMP0000";
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        // Increment the last employee ID
        int numericPart = Integer.parseInt(lastEmployeeID.substring(3));
        numericPart++;

        // Customize the employee ID format
        employeeID = String.format("EMP%04d", numericPart);

        jTextField3.setText(employeeID);
    }

    private void loadEmployees() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `employee` ORDER BY `registered_date` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector1 = new Vector<>();
                vector1.add(resultSet.getString("employee_id"));
                vector1.add(resultSet.getString("first_name"));
                vector1.add(resultSet.getString("last_name"));
                vector1.add(resultSet.getString("mobile"));
                vector1.add(resultSet.getString("password"));
                vector1.add(resultSet.getString("registered_date"));

                model.addRow(vector1);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

