    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ID = jTextField3.getText();
        String first_name = jTextField1.getText();
        String last_name = jTextField2.getText();
        String mobile = jTextField4.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        if (first_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter first name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (last_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter last name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter password.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Date date1 = new Date();
            SimpleDateFormat format1 = new SimpleDateFormat("YYYY-MM-dd");
            String stringDate1 = format1.format(date1);

            try {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `employee` WHERE `first_name`='" + first_name + "' AND `last_name`='" + last_name + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This employee already exsist.", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.execute("INSERT INTO `employee` (`employee_id`,`first_name`,`last_name`,`mobile`,`password`,`registered_date`)"
                            + "VALUES ('" + ID + "','" + first_name + "','" + last_name + "','" + mobile + "','" + password + "','" + stringDate1 + "')");

                    loadEmployees();
                    generateEmployeeID();
                    reset();

                }

            } catch (Exception e) {
                MainInterface.logger.log(Level.WARNING, "Employee registration.", e);
                //e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

