    private void generateExpensesNo() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `expenses` ORDER BY `expenses_no` DESC LIMIT 1");
            if (resultSet.next()) {
                lastexpno = resultSet.getString("expenses_no");

            } else {
                lastexpno = "EXP0000";
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        // Increment the last employee ID
        int numericPart = Integer.parseInt(lastexpno.substring(3));
        numericPart++;

        // Customize the employee ID format
        expno = String.format("EXP%04d", numericPart);

        jTextField1.setText(expno);
    }

