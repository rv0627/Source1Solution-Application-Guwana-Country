    public double gasexp = 0;
    public double transexp = 0;
    public double purchases = 0;
    public double rent = 0;
    public double Miscellaneous = 0;
    public double Freight_Forwarding_Charges = 0;
    public double total = 0;

    private void calculate() {
        if (jFormattedTextField1.getText().isEmpty()) {
            gasexp = 0;
        } else {
            gasexp = Double.parseDouble(jFormattedTextField1.getText());
        }
        if (jFormattedTextField2.getText().isEmpty()) {
            transexp = 0;
        } else {
            transexp = Double.parseDouble(jFormattedTextField2.getText());
        }
        if (jFormattedTextField3.getText().isEmpty()) {
            purchases = 0;
        } else {
            purchases = Double.parseDouble(jFormattedTextField3.getText());
        }
        if (jFormattedTextField4.getText().isEmpty()) {
            rent = 0;
        } else {
            rent = Double.parseDouble(jFormattedTextField4.getText());
        }
        if (jFormattedTextField5.getText().isEmpty()) {
            Miscellaneous = 0;
        } else {
            Miscellaneous = Double.parseDouble(jFormattedTextField5.getText());
        }
        if (jFormattedTextField6.getText().isEmpty()) {
            Freight_Forwarding_Charges = 0;
        } else {
            Freight_Forwarding_Charges = Double.parseDouble(jFormattedTextField6.getText());
        }
        total = (gasexp + transexp + purchases + rent + Miscellaneous + Freight_Forwarding_Charges);
        jFormattedTextField7.setText(String.valueOf(total));
    }

