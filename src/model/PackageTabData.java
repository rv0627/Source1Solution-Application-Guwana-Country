package model;

/**
 *
 * @author ravin
 */
public class PackageTabData {
    private String wireceipt;
    private String customerName;
    private String pacWeight;
    private String noPac;
    private String desPac;
    private String status;
    private String date;

    
    public String getWireceipt() {
        return wireceipt;
    }

    
    public void setWireceipt(String wireceipt) {
        this.wireceipt = wireceipt;
    }

    /**
     * @return the customerName
     */
    public String getCustomerName() {
        return customerName;
    }

    /**
     * @param customerName the customerName to set
     */
    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    /**
     * @return the pacWeight
     */
    public String getPacWeight() {
        return pacWeight;
    }

    /**
     * @param pacWeight the pacWeight to set
     */
    public void setPacWeight(String pacWeight) {
        this.pacWeight = pacWeight;
    }

    /**
     * @return the noPac
     */
    public String getNoPac() {
        return noPac;
    }

    /**
     * @param noPac the noPac to set
     */
    public void setNoPac(String noPac) {
        this.noPac = noPac;
    }

    /**
     * @return the desPac
     */
    public String getDesPac() {
        return desPac;
    }

    /**
     * @param desPac the desPac to set
     */
    public void setDesPac(String desPac) {
        this.desPac = desPac;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }
    
}
